    private void btnMostrarPersonasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarPersonasActionPerformed
        FirestoreData fd = new FirestoreData();
        List<Usuario> personas = fd.mostrarPersona();
        DefaultListModel<Usuario> listModel = new DefaultListModel<Usuario>();
        for (Usuario per : personas) {
            listModel.addElement(per);
        }
        listUsuarios.setModel(listModel);
    }//GEN-LAST:event_btnMostrarPersonasActionPerformed

    private void listUsuariosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listUsuariosValueChanged
        Usuario per = listUsuarios.getSelectedValue();
        txtMostrarID.setText(per.getID());
        txtMostrarNombre.setText(per.getName());
        txtShowLastName.setText(per.getLastName());
        txaShowAddress.setText(per.getAddress());
        txtShowPhoneNumber.setText(per.getPhoneNumber());
        txtShowEmail.setText(per.getEmail());
        txtShowLE.setText(per.getLE());
        txaShowDescription.setText(per.getLaboralEx());
        txaShowAcademicTitles.setText(per.getTitulos());
        txaShowLanguages.setText(per.getLanguages());
        txaShowProjecDescription.setText(per.getExtras());
    }//GEN-LAST:event_listUsuariosValueChanged

